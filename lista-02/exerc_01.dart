/// Crie uma função em Dart chamada soma que receba dois números como parâmetros,
/// calcule a soma desses números e retorne o resultado. Chame a função e imprima o resultado.

int soma(int a, int b) {
  return a + b;
}

void main() {
  int resultado = soma(5, 10);
  print("A soma é: $resultado");
}
